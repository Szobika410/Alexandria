import mongoose from "mongoose";

const profileSchema = new mongoose.Schema({
  clerkUserId: {
    type: String,
    required: true,
    unique: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  username: {
    type: String,
    required: true
  },
  firstName: {
    type: String
  },
  lastName: {
    type: String
  },
  image: {
    type: String
  }
}, {
  timestamps: true
});

export default mongoose.models.Profile || mongoose.model("Profile", profileSchema);
